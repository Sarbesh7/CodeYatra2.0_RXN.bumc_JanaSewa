/**
 * Get a template by key
 * @param {string} key - The template key
 * @returns {Object|null} - The template object or null if not found
 */
export const getTemplate = (key) => {
  return SAMPLE_TEMPLATES[key] || null;
};

/**
 * Get all available templates
 * @returns {Array} - Array of template objects with their keys
 */
export const getAllTemplates = () => {
  return Object.entries(SAMPLE_TEMPLATES).map(([key, template]) => ({
    key,
    ...template,
  }));
};
